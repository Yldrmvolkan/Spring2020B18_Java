package switch_statement;

public class switchstatement {
    public static void main(String[] args) {
        switch (4) {
            case 1:
                System.out.println("one");
                break;

            case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("invalid");
            case 3:
                System.out.println("uc");
                break;
        }
    }
}
