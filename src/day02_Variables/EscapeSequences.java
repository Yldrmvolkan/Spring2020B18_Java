package day02_Variables;


public class EscapeSequences {

   public static void main(String[] args) {
      System.out.println("\tajksfhaiug");
      System.out.println("dfgsth\nsjharig");
      System.out.println("assfasg\"hgajhrgaleyrg\"");
      System.out.println("my favorite book is \"fg books\"");
      System.out.println("\\asdsefaersrth\\");
      System.out.println("sdfasjg 'sdfas' ");
      System.out.println("\'dfgdf\'");

   }

}
